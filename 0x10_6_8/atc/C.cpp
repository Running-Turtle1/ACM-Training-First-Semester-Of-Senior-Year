#include<bits/stdc++.h>
using namespace std;
#define int long long

int a[750][750];

int ksm(int a, int b){
	int res = 1;
	while(b){
		res = res * a;
		b --;
	}
	return res;
}

// LU : (x, y) ; 染成 lv 级别
void dfs(int lv, int x, int y){
	if(lv == 0){
		a[x][y] = 1; // 黑色
		return ;
	}

	for(int i = 0; i < 3; i ++){
		for(int j = 0; j < 3; j ++){
			int step = ksm(3, lv - 1);
			int stx = x + step * i, sty = y + step * j;
			if(i == 1 && j == 1){
				for(int k1 = stx; k1 <= stx + step - 1; k1 ++){
					for(int k2 = sty; k2 <= sty + step - 1; k2 ++){
						a[k1][k2] = 0;
					}
				}
				continue;
			}
			dfs(lv - 1, stx, sty);
		}
	}
}

void solve(){
	int n;
	cin >> n;
	dfs(n, 1, 1);
	int mx = ksm(3, n);
	for(int i = 1; i <= mx; i ++){
        for(int j = 1; j <= mx; j ++){
            cout << (a[i][j] ? "#" : ".");
        }
        cout << '\n';
    }
}

signed main(){
	ios::sync_with_stdio(false);
	cin.tie(0), cout.tie(0);

	int T = 1;
	while (T --){
		solve();
	}

	return 0;
}
